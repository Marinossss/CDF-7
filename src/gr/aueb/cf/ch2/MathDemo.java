package gr.aueb.cf.ch2;
/**
 * Math class demo.
 */
public class MathDemo {
    public static void main(String[] args) {
        System.out.println("To apolyto toy -5 einai " + Math.abs(-5)); //abs κανει το απολυτο αυτου που δηλωνουμε
        System.out.println("To min tou 3, 19 einai " + Math.min(3, 19)); //το λγοτερο
        System.out.println("To max toy -5 einai " + Math.max(-5, 299)); //το περισοτερο
    }
}
